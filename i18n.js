let currentLanguage = localStorage.getItem('agriride_language') || 'en';

// Set language class instantly on document root
document.documentElement.className = document.documentElement.className
  .split(' ')
  .filter(c => !c.startsWith('lang-'))
  .join(' ');
document.documentElement.classList.add('lang-' + currentLanguage);

function setLanguage(langCode) {
  if (TRANSLATIONS[langCode]) {
    currentLanguage = langCode;
    localStorage.setItem('agriride_language', langCode);
    
    // Update language class on root
    document.documentElement.className = document.documentElement.className
      .split(' ')
      .filter(c => !c.startsWith('lang-'))
      .join(' ');
    document.documentElement.classList.add('lang-' + langCode);
    
    applyTranslations();
  }
}

function t(key) {
  if (TRANSLATIONS[currentLanguage] && TRANSLATIONS[currentLanguage][key]) {
    return TRANSLATIONS[currentLanguage][key];
  }
  // Fallback to English
  if (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) {
    return TRANSLATIONS['en'][key];
  }
  return key; // return key itself if translation not found
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translatedText = t(key);
    
    // Check if element has child nodes that aren't just text nodes
    // to preserve structure if needed. For simplicity, if innerHTML contains tags, 
    // we should be careful, but basic textContent replacement is safer for generic text elements.
    
    // Special handling for inputs with placeholders
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        if(el.hasAttribute('placeholder')) {
            el.placeholder = translatedText;
        } else {
             el.value = translatedText;
        }
    } else {
        // If element contains an icon (e.g. <i>), preserve it
        const icon = el.querySelector('i');
        const badge = el.querySelector('.badge');
        
        if (icon) {
            let html = icon.outerHTML + ' ' + translatedText;
            if(badge) html += ' ' + badge.outerHTML;
            el.innerHTML = html;
        } else {
             el.textContent = translatedText;
        }
    }
  });

  // Also trigger custom event in case components need to re-render
  document.dispatchEvent(new Event('languageChanged'));
}

function getCurrentLanguage() {
  return currentLanguage;
}

function getLanguageList() {
  return LANGUAGES;
}

// Initial application of translations
document.addEventListener('DOMContentLoaded', () => {
    applyTranslations();
});

// Expose to window for global access
window.i18n = {
  setLanguage,
  t,
  applyTranslations,
  getCurrentLanguage,
  getLanguageList
};
